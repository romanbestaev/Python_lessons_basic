for i in range(0,2500):
    fid.write(str(random.randint(0,9)))
fid.close()
line_3 = open('random_digits.txt','r').read()
max_line = str(line_3[0])
x_line = ''
for i in range(1,len(line_3)):
    if line_3[i] == line_3[i-1]:
        x_line += str(line_3[i])
        if len(x_line) > len(max_line):
            max_line = x_line
    else:
        x_line = str(line_3[i])
